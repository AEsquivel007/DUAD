def test_bubble_sort_with_empty_list():
    list_to_sort = []
    
    bubble_sort(list_to_sort)
    
    assert list_to_sort == []


def test_bubble_sort_throws_exception_without_only_numbers():
    
    list_to_sort = [90,-47,1,6,-3,300,-6, "Hello"]
    
    with pytest.raises(TypeError):
        bubble_sort(list_to_sort)


def test_bubble_sort_throws_exception_without_a_list_as_argument():
    
    dict_to_sort = {"name": "Python"}
    
    with pytest.raises(TypeError):
        bubble_sort(dict_to_sort)